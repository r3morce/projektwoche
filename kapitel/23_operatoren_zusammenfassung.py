# === Die vier Grundrechenarten ===

# 1. Addition mit +
print("Addition:")
print("5 + 3 =", 5 + 3)
print()

# 2. Subtraktion mit -
print("Subtraktion:")
print("7 - 4 =", 7 - 4)
print()

# 3. Multiplikation mit *
print("Multiplikation:")
print("6 * 4 =", 6 * 4)
print()

# 4. Division mit /
print("Division:")
print("8 / 2 =", 8 / 2)
print()

# === Wichtige Regeln ===

# 1. Punkt vor Strich
print("Punkt vor Strich:")
print("2 + 3 * 4 =", 2 + 3 * 4)     # erst 3 * 4, dann + 2
print("(2 + 3) * 4 =", (2 + 3) * 4) # erst (2 + 3), dann * 4
print()

# 2. Klammern haben Vorrang
print("Mit Klammern:")
print("4 + (5 - 2) =", 4 + (5 - 2))
print()

# === Checkliste ===
# ✓ + für Addition (Plus)
# ✓ - für Subtraktion (Minus)
# ✓ * für Multiplikation (Mal)
# ✓ / für Division (Geteilt)

# === Merkhilfen ===
# - Der Stern * bedeutet "mal"
# - Der Schrägstrich / bedeutet "geteilt"
# - Klammern machen die Reihenfolge klar
# - Division ergibt immer eine Dezimalzahl
