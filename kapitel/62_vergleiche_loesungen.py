# Lösung 1: Zahlenvergleiche
print("=== Zahlenvergleiche ===")
print("10 > 7:", 10 > 7)    # True
print("10 < 7:", 10 < 7)    # False
print("10 == 7:", 10 == 7)  # False
print("10 != 7:", 10 != 7)  # True
print()

# Lösung 2: Spielerbewertung
punkte = 75
print("=== Spielerbewertung ===")
print("Mindestens 50 Punkte?", punkte >= 50)  # True
print("Unter 100 Punkte?", punkte < 100)      # True
print("Genau 75 Punkte?", punkte == 75)       # True
print()

# Lösung 3: Quizfragen
print("=== Quiz-Vergleiche ===")
print("15 > 20:", 15 > 20)     # False
print("30 >= 30:", 30 >= 30)   # True
print("42 != 42:", 42 != 42)   # False
print("100 <= 99:", 100 <= 99) # False
