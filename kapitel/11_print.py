# 01_print.py
# ===========
# THEMA: Der print()-Befehl in Python
# Mit print() können wir Text und Zahlen auf dem Bildschirm ausgeben.
# ===========

# ========== ERKLÄRUNG ==========
print("=== Der print-Befehl ===")

# 1. Text ausgeben (braucht Anführungszeichen!)
print("Hallo Welt!")
print('Auch einfache Anführungszeichen gehen!')

# 2. Zahlen ausgeben (keine Anführungszeichen nötig)
print(42)
print(3.14)

# 3. Text und Zahlen kombinieren (mit Komma trennen)
print("Ich bin", 16, "Jahre alt")
print("Die Temperatur ist", 20, "Grad")

# 4. Leerzeilen einfügen
print()  # Erzeugt eine leere Zeile

# Wichtige Regeln:
# - Text immer in Anführungszeichen
# - Zahlen können ohne Anführungszeichen geschrieben werden
# - Mehrere Dinge mit Komma trennen
# - Nach print immer Klammern!

