# ========== ERKLÄRUNG ==========
print("=== Variablen verstehen ===\n")

# Eine Variable ist wie eine beschriftete Box
# Hier legen wir Werte in verschiedene Boxen:
name = "Alex"              # Text kommt in Anführungszeichen
alter = 15                # Zahlen ohne Anführungszeichen
temperatur = 19.5         # Dezimalzahlen mit Punkt

# Wir können uns den Inhalt der Boxen ansehen:
print("Name:", name)
print("Alter:", alter)
print("Temperatur:", temperatur)
print()

# Wir können den Inhalt einer Box auch ändern:
name = "Max"              # Neuer Name in die Box
print("Neuer Name:", name)
print()

# ========== BEISPIELE ==========
