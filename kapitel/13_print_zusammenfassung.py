# =========================
# THEMA: Zusammenfassung des print()-Befehls
# =========================

# === Die wichtigsten Punkte ===

# 1. Textausgabe
print("Text steht in Anführungszeichen")

# 2. Zahlenausgabe
print(42)  # Zahlen ohne Anführungszeichen

# 3. Kombinierte Ausgabe
print("Anzahl:", 42)  # Mit Komma getrennt

# 4. Leerzeilen
print()  # Erzeugt eine Leerzeile

# === Checkliste ===
# ✓ Wurde bei Text Anführungszeichen verwendet?
# ✓ Wurden bei Zahlen keine Anführungszeichen verwendet?
# ✓ Wurden die runden Klammern nach print gesetzt?
# ✓ Wurden mehrere Werte mit Komma getrennt?

# === Häufige Fehler ===
# ✗ print "Hallo"        # Fehler: Klammern fehlen
# ✗ print(Hallo)         # Fehler: Anführungszeichen fehlen
# ✗ print('Das sind" 42) # Fehler: Verschiedene Anführungszeichen

