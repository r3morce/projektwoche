# =================================================================

# 1. KOMMENTARE
# Einzeilige Kommentare beginnen mit #
print("Code mit Kommentar")  # Kommentar am Ende der Zeile
# Ganze Zeilen können auskommentiert werden

# 2. GRUNDFORM VON PRINT
print("Hello World!")   # Das erste Programm

# 3. VERSCHIEDENE ARTEN VON AUSGABEN
print(42)              # Zahlen direkt
print("Text", 42)      # Text und Zahlen mischen
print("A", "B", "C")   # Mehrere Werte werden automatisch getrennt

# 4. FORMATIERUNG
print()                # Leere Zeile

# 5. WICHTIGE REGELN
# - Kommentare beginnen mit # und werden von Python ignoriert
# - Text immer in Anführungszeichen (" oder ')
# - Zahlen ohne Anführungszeichen
# - Kommazahlen mit Punkt schreiben (3.14)
# - Mehrere Werte mit Komma trennen

